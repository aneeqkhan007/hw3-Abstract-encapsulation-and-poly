package Encapsulation;

public class Watch {
    private String names;
    private int straps;
    private  int gems;

    public String getNames() {
        return names;
    }

    public void setNames(String names) {
        this.names = names;
    }

    public int getStraps() {
        return straps;
    }

    public void setStraps(int straps) {
        this.straps = straps;
    }

    public int getGems() {
        return gems;
    }

    public void setGems(int gems) {
        this.gems = gems;
    }
}
