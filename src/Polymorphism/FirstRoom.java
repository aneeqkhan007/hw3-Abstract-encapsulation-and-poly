package Polymorphism;

public class FirstRoom {

    public void bulb() {

    }

    public int bulb(int x) {
        int sum = x;
        return sum;

    }

    public int bulb(int x, int y) {
        int sum = x + y;
        return sum;

    }

    public int bulb(int x, int y, int z) {
        int sum = x + y + z;
        return sum;

    }
}
